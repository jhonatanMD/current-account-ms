package com.current.account.model;


public class HeadLineEntity {


	private String namH;
	private String lastNamH;
	private String docClien;
	
	
	public String getNamH() {
		return namH;
	}
	public void setNamH(String namH) {
		this.namH = namH;
	}
	public String getLastNamH() {
		return lastNamH;
	}
	public void setLastNamH(String lastNamH) {
		this.lastNamH = lastNamH;
	}
	public String getDocClien() {
		return docClien;
	}
	public void setDocClien(String docClien) {
		this.docClien = docClien;
	}

	
	
	
}
